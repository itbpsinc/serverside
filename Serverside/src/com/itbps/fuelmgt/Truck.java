package com.itbps.fuelmgt;

public class Truck {
	private String truckname;
	private String truckdescription;
	private double lastmainmilage;

	public String getTruckname() {
		return truckname;
	}

	public void setTruckname(String truckname) {
		this.truckname = truckname;
	}

	public String getTruckdescription() {
		return truckdescription;
	}

	public void setTruckdescription(String truckdescription) {
		this.truckdescription = truckdescription;
	}

	public double getLastmainmilage() {
		return lastmainmilage;
	}

	public void setLastmainmilage(double lastmainmilage) {
		this.lastmainmilage = lastmainmilage;
	}

}
