package com.itbps.fuelmgt;

public class Compactment {
	private String compactmentid;
	private String compactment;
	private String truckname;
	private String capacity;

	public String getCompactmentid() {
		return compactmentid;
	}

	public void setCompactmentid(String compactmentid) {
		this.compactmentid = compactmentid;
	}

	public String getCompactment() {
		return compactment;
	}

	public void setCompactment(String compactment) {
		this.compactment = compactment;
	}

	public String getTruckname() {
		return truckname;
	}

	public void setTruckname(String truckname) {
		this.truckname = truckname;
	}

	public String getCapacity() {
		return capacity;
	}

	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}

}
